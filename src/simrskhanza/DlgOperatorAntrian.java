    private void cekAntrian() {
        panggilan = 0;
        nomorpasien = 0;
        hasilantrian = 0;

        //cek nomor antrian pasien
        if (Sequel.cariIsi("select no_antrian from antrian_nomor where date(waktu_cetak)=date(now()) order by no_antrian desc limit 1").equals("")) {
            nomorpasien = 0;
        } else {
            nomorpasien = Integer.parseInt(Sequel.cariIsi("select no_antrian from antrian_nomor where date(waktu_cetak)=date(now()) order by no_antrian desc limit 1"));
        }

        //cek nomor panggilan pasien
        if (Sequel.cariIsi("SELECT no_antrian FROM antrian_pemanggil where date(waktu_panggil)=date(now()) ORDER BY no_antrian DESC LIMIT 1").equals("")) {
            panggilan = 0;
        } else {
            panggilan = Integer.parseInt(Sequel.cariIsi("select no_antrian from antrian_pemanggil where date(waktu_panggil)=date(now()) order by no_antrian desc limit 1"));
        }

        if (panggilan == 0 && nomorpasien == 0) {
            infoantrian.setText("Hari ini masih belum ada pasien.");
        } else {
            infoantrian.setText("Antrian terakhir pasien adalah nomor : " + nomorpasien + "");

            //hasil hitungan antrian
            hasilantrian = nomorpasien - panggilan;
            if (nomorpasien > panggilan) {
                infoantrian.setText("Ada antrian pasien rawat jalan yang belum dipanggil & dilayani oleh petugas pendaftaran.");
            }

            if (hasilantrian >= 1) {
                infoantrian.setText("Masih ada : " + hasilantrian + " orang lagi yang belum dipanggil & dilayani oleh petugas pendaftaran pasien rawat jalan.");
            }

            if (hasilantrian < 1) {
                infoantrian.setText("Antrian pasien rawat jalan sudah habis.");
            }

            if (panggilan > nomorpasien) {
                infoantrian.setText("Ada antrian pasien rawat jalan yang terlewati dari panggilan operator.");
            }
        }
    }
