    private void BtnPanggilanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnPanggilanActionPerformed
        cekPang = 0;
        if (akses.getNomorLoket().equals("1") || akses.getNomorLoket().equals("2")
                || akses.getNomorLoket().equals("3") || akses.getNomorLoket().equals("4")
                || akses.getNomorLoket().equals("5") || akses.getNomorLoket().equals("6")) {
            cekPang = Sequel.cariInteger("select count(-1) from antrian_pemanggil where status='proses' and date(waktu_panggil)=date(now())");

            if (infoantrian.getText().equals("Hari ini masih belum ada pasien.")) {
                JOptionPane.showMessageDialog(null, "Belum ada pasien yang mengambil nomor antrian,...!!");
            } else if (cekPang > 0) {
                JOptionPane.showMessageDialog(null, "Loket lain masih memanggil antrian, tunggu sebentar,...!!");
            } else if (infoantrian.getText().equals("Antrian pasien rawat jalan sudah habis.")) {
                JOptionPane.showMessageDialog(null, "Hentikan panggilan antrian pasien karena sudah habis dilayani, terima kasih...!!");
            } else {
                tambahPanggilan();
                suaraPanggilan();
                Sequel.mengedit("antrian_pemanggil", "status='proses' and date(waktu_panggil)=date(now())", "status='ok'");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Silahkan tentukan dulu nomor loketnya utk. melakukan panggilan antrian & simpan sbg. operator..!!");
        }
    }//GEN-LAST:event_BtnPanggilanActionPerformed

    private void BtnUlangPanggilanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnUlangPanggilanActionPerformed
        cekPang = 0;

        if (akses.getNomorLoket().equals("1") || akses.getNomorLoket().equals("2")
                || akses.getNomorLoket().equals("3") || akses.getNomorLoket().equals("4")
                || akses.getNomorLoket().equals("5") || akses.getNomorLoket().equals("6")) {
            cekPang = Sequel.cariInteger("select count(-1) from antrian_pemanggil where status='proses' and date(waktu_panggil)=date(now())");

            if (infoantrian.getText().equals("Hari ini masih belum ada pasien.")) {
                JOptionPane.showMessageDialog(null, "Belum ada pasien yang mengambil nomor antrian,...!!");
            } else if (cekPang > 0) {
                JOptionPane.showMessageDialog(null, "Loket lain masih memanggil antrian, tunggu sebentar,...!!");
            } else if (infoantrian.getText().contains("Masih ada") == true && nomor.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Lakukan panggilan dulu, kemudian panggilannya baru bisa diulangi...!!");
            } else {
                Sequel.mengedit("antrian_pemanggil", "no_antrian='" + Sequel.cariIsi("SELECT no_antrian FROM antrian_pemanggil where date(waktu_panggil)=date(now()) ORDER BY no_antrian DESC LIMIT 1") + "'",
                        "status='proses'");
                suaraPanggilan();
                Sequel.mengedit("antrian_pemanggil", "no_antrian='" + Sequel.cariIsi("SELECT no_antrian FROM antrian_pemanggil where date(waktu_panggil)=date(now()) ORDER BY no_antrian DESC LIMIT 1") + "'",
                        "status='ok'");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Silahkan tentukan dulu nomor loketnya utk. melakukan panggilan antrian & simpan sbg. operator..!!");
        }
    }//GEN-LAST:event_BtnUlangPanggilanActionPerformed

